#include <stdlib.h>
#include <time.h>
#include <stdio.h>
/**
 * main -Determines if a number is positive, negative zero
 * Description: 'the program's description'
 * Return: Always 0 (Success)
 */
int main(void)
{
	char c;

	c = 'a';
	while
		(c <= 'z') {
			if ((c != 'q' && c != 'e') && c <= 'z')
				putchar(c);
			c++;
		}
	putchar('\n');
	return (0);
}
